class Card
  include Comparable
  attr_reader :rank, :suit

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(rank, rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

class Deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    reset
  end

  def draw
    reset if @deck.empty?
    @deck.pop
  end

  private

  def reset
    @deck = RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end

    @deck.shuffle!
  end
end

class PokerHand
  def initialize(cards)
    @cards = []
    @rank_count = Hash.new(0)

    5.times do
      card = cards.draw
      @cards << card
      @rank_count[card.rank] += 1
    end
  end

  def print
    puts @cards
  end

  def evaluate
    if    royal_flush?     then 'Royal flush'
    elsif straight_flush?  then 'Straight flush'
    elsif four_of_a_kind?  then 'Four of a kind'
    elsif full_house?      then 'Full house'
    elsif flush?           then 'Flush'
    elsif straight?        then 'Straight'
    elsif three_of_a_kind? then 'Three of a kind'
    elsif two_pair?        then 'Two pair'
    elsif pair?            then 'Pair'
    else 'High card'
    end
  end

  private

  def flush?
    # Any five cards of the same suit, but not in a sequence. 
    suit = @cards.first.suit
    @cards.all? { |card| card.suit == suit }
  end

  def straight?
    # Five cards in a sequence, but not of the same suit.
    return false if @rank_count.any? { |_, count| count > 1 }

    @cards.min.value == @cards.max.value - 4
  end

  def n_of_a_kind?(number)
    @rank_count.one? { |_, count| count == number }
  end

  def straight_flush?
    # Five cards in a sequence, all in the same suit.
    flush? && straight?
  end

  def royal_flush?
     # A, K, Q, J, 10, all the same suit. 
    straight_flush? && @cards.min.rank == 10
  end

  def four_of_a_kind?
    # All four cards of the same rank. 
    n_of_a_kind?(4)
  end

  def full_house?
    # Three of a kind with a pair. 
    n_of_a_kind?(3) && n_of_a_kind?(2)
  end

  def three_of_a_kind
    # Three cards of the same rank. 
    n_of_a_kind?(3)
  end

  def two_pair?
    # Two different pairs.
    @rank_count.select { |_, count| count == 2 }.size == 2
  end

  def pair?
    # Two cards of the same rank. 
    n_of_a_kind?(2)
  end
end

