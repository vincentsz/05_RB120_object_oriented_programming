# What is Object Oriented Programming, and why was it created? What are the benefits of OOP, and examples of problems it solves?

# OOP is a programming paradigm developed to deal with growing complexity of large software systems.
# Data is organized in containers that can be manipulated without affecting, through its dependencies, the rest of the program.
# Any changes you make to the program are encapsulated to a class or object.
# This means that the interface that interacts with the class or object can remain while the implementation can change.
# Creating objects allows coders to think more abstractly about the software they are writing.
# Since objects are represented by nouns they are easier to conceptualize.
# It allows us to expose functionality only to parts of the code thus reducing namespacing issues.
# It allows us to give functionality to different parts of an application without duplication.
# We can build apps faster since we can reuse code.
# Complexity of software projects can be more easily managed.
# Both encapsulation and polymorphism are fundamental concepts of OOP.